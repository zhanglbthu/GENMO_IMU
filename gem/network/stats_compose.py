def compose(targets, sources):
    """Compose stats from multiple source distributions."""
    if len(sources) == 1:
        sources = sources * len(targets)
    mean = []
    std = []
    for t, s in zip(targets, sources):
        mean.extend(t[s]["mean"])
        std.extend(t[s]["std"])
    return {"mean": mean, "std": std}


